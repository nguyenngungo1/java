    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableCategory.getModel();
        try {
            Connection con = ConnectionProvider.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM Loaihang ORDER BY ID_LH ASC");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("ID_LH"), rs.getString("Name")});
            }
            // Đóng ResultSet và Statement sau khi sử dụng
            rs.close();
            st.close();
            con.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        String name = txtName.getText();
        if (validateFields()) {
            JOptionPane.showConfirmDialog(null, "cần nhập đủ thông tin");
        } else {
            try {
                Connection con = ConnectionProvider.getConnection();
                String query = "insert into Loaihang(Name) values (?)";
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, name);
                pst.executeQuery();
                JOptionPane.showMessageDialog(null, "đã thêm loại hàng mới");
                setVisible(false);
                new ManageCategory().setVisible(true);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

