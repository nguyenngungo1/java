    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        
        int confirmDelete = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa loại hàng này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);

    if (confirmDelete == JOptionPane.YES_OPTION) {
        try {
            Connection con = ConnectionProvider.getConnection();
            String query = "DELETE FROM Loaihang WHERE ID_LH = ?";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setInt(1, ID_LH);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Xóa thành công");
            setVisible(false);
            new ManageCategory().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

        setVisible(false);
        new ManageCategory().setVisible(true);
    }//GEN-LAST:event_btndeleteActionPerformed

    private void tableCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCategoryMouseClicked
        int index = tableCategory.getSelectedRow();
        TableModel model = tableCategory.getModel();
        String Id = model.getValueAt(index, 0).toString();
        ID_LH = Integer.parseInt(Id);

        String Name = model.getValueAt(index, 1).toString();
        txtName.setText(Name);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableCategoryMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        if (validateFields()) {
            JOptionPane.showConfirmDialog(null, "cần nhập đủ thông tin");
        } else {
            try {
                Connection con = ConnectionProvider.getConnection();
                String query = "update Loaihang set Name=? Where ID_LH=?";
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, name);
                pst.setInt(2, ID_LH);
                pst.executeQuery();
                JOptionPane.showMessageDialog(null, "cập nhập thành công");
                setVisible(false);
                new ManageCategory().setVisible(true);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

